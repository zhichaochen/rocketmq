/**
 * Store all metadata downtime for recovery, data protection reliability
 *
 * 存储所有元数据停机时间以实现恢复、数据保护可靠性
 *
 * RocketMQ 刷盘支持同步刷盘和异步刷盘。
 *
 * Comitlog#putMessage 首先将消息写入到 MappedFile,内存映射文件。然后根据刷盘策略刷写到磁盘
 *
 * 入口如下：handleDiskFlush
 */
